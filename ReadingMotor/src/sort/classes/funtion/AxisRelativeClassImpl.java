package sort.classes.funtion;

public class AxisRelativeClassImpl implements AxisRelativeClass {

	protected int x1;

	protected int x2;

	protected int dy;

	public int getX1() {
		return x1;
	}

	public void setX1(int x1) {
		this.x1 = x1;
	}

	public int getX2() {
		return x2;
	}

	public void setX2(int x2) {
		this.x2 = x2;
	}

	public int getDy() {
		return dy;
	}

	public void setDy(int dy) {
		this.dy = dy;
	}
}
